// Sample use of the builder you are asked to create:

/*
    CodeBuilder cb = new CodeBuilder("Person").addField("name", "String").addField("age", "int");
    System.out.println(cb);
 */

// The expected output of the above code is:

/*
    public class Person {
        public String name;
        public int age;
    }
 */

